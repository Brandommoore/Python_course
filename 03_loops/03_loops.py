# for loop
# some print annotations

for i in range(5):
    print(f"var value {i}")  # Anotacion especial para concatenar str y var

print("\n\n")

for i in range(5, 10):  # Cuenta desde el 5 hasta el 10
    print(f"var value {i}")

print("\n\n")

for i in range(5, 50, 3):  # El tercer parametro indica de cuanto en cuanto vale el paso
    print(f"var value {i}")